public class YourInitials
{
    public static void main( String[] args )
    {
        System.out.println( "      A        H       H" ); // and the comment after is ignored.
        System.out.println( "     A A       H       H " );
        System.out.println( "    A   A      HHHHHHHHH ");
        System.out.println( "   AAAAAAA     H       H" );
        System.out.println( "  A       A    H       H " );
        System.out.println( " A         A   H       H " );
    }
}
